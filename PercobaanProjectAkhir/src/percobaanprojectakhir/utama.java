    private void updateComboBoxdata() {
    jComboBox1.removeAllItems();
    jComboBox2.removeAllItems();
        try {
            String sql = "SELECT namaPasienSakit FROM tambahdatasakit";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("namaPasienSakit"));
                jComboBox2.addItem(rs.getString("namaPasienSakit"));
            }
        } catch (SQLException e) {
            System.out.println("Error Update ComboBox Karyawan: " + e.getMessage());
        }
    }
