    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            // Query untuk menambahkan data
            String sql = "INSERT INTO tambahJadwal (namaPasienSakit, tanggalCheck, alamatCheck) VALUES (?, ?, ?)";
            PreparedStatement statement = conn.prepareStatement(sql);
            String namapasien = jComboBox1.getSelectedItem().toString();
            String tanggalCheck = jTextField2.getText();
            String alamatCheck = jTextField3.getText();

            // Set parameter ke query
            statement.setString(1, namapasien);
            statement.setString(2, tanggalCheck);
            statement.setString(3, alamatCheck);

        int rowsInserted = statement.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Data Upload successfully");
            jComboBox1.setSelectedIndex(0);
            jTextField3.setText("");
            jTextField4.setText("");
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

